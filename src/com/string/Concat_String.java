package com.string;

public class Concat_String {

	public static void main(String[] args) {
		// Manju Kumari -4564756, Shruti Katkar -586758, Swati Pathak -755864
		
		String s= "Manju".concat(" : Kumari").concat(" -4564756");
		
		String s2="Shruti".concat(" : Katkar").concat(" -586758");
		
		String s3="Swati".concat(" : Pathak").concat(" -755864");
		System.out.println(s);
		System.out.println(s2);
		System.out.println(s3);

	}

}
